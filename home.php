			function getTimeRemaining(endtime, week){
				setInterval(function(){
					var t = (endtime*1000) - Math.round((new Date()).getTime());
					var seconds = Math.floor((t/1000) % 60 );
					var minutes = Math.floor((t/1000/60) % 60 );
					var hours = Math.floor((t/(1000*60*60)) % 24 );
					var days = Math.floor((t/(1000*60*60*24) % 7 ));

					if(seconds <= 0 && minutes <= 0 && hours <= 0){
						if(week === true){
							$('#timer').html('00:00:00:00');
						}else{
							$('#timer').html('00:00:00');
						}
						setTimeout(function(){
							//document.location.reload(true);
						}, 1000);
						return;
					}else{
						if(week === true){
							let html = '<div class="d-inline-block"><font class="mx-2 px-2 py-1 border border-success">' + ("0" + days).slice(-2) + ' </font> <p class="fs1 mt-2 text-muted card-text">Days</p></div>';
							html += '<div class="d-inline-block"><font class="mx-2 px-2 py-1 border border-success">' + ("0" + hours).slice(-2) + ' </font> <p class="fs1 mt-2 text-muted card-text">Hours</p></div>';
							html += '<div class="d-inline-block"><font class="mx-2 px-2 py-1 border border-success">' + ("0" + minutes).slice(-2) + ' </font> <p class="fs1 mt-2 text-muted card-text">Minutes</p></div>';
							html += '<div class="d-inline-block"><font class="mx-2 px-2 py-1 border border-success">' + ("0" + seconds).slice(-2) + ' </font> <p class="fs1 mt-2 text-muted card-text">Seconds</p></div>';
							
							$('#timer').html(html);
						}else{
							let html = '<div class="d-inline-block"><font class="mx-2 px-2 py-1 border border-success">' + ("0" + hours).slice(-2) + ' </font> <p class="fs1 mt-2 text-muted">Hours</p></div>';
							html += '<div class="d-inline-block"><font class="mx-2 px-2 py-1 border border-success">' + ("0" + minutes).slice(-2) + ' </font> <p class="fs1 mt-2 text-muted card-text">Minutes</p></div>';
							html += '<div class="d-inline-block"><font class="mx-2 px-2 py-1 border border-success">' + ("0" + seconds).slice(-2) + ' </font> <p class="fs1 mt-2 text-muted card-text">Seconds</p></div>';
							$('#timer').html(html);
						}
					}
				}, 1000);
			}
			
			
